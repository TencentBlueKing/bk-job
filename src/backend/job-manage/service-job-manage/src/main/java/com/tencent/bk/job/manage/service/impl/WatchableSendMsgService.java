package com.tencent.bk.job.manage.service.impl;

import com.tencent.bk.job.common.constant.ErrorCode;
import com.tencent.bk.job.common.esb.metrics.EsbApiTimed;
import com.tencent.bk.job.common.exception.ResourceExhaustedException;
import com.tencent.bk.job.common.model.dto.ResourceScope;
import com.tencent.bk.job.common.paas.cmsi.ICmsiClient;
import com.tencent.bk.job.common.paas.exception.PaasException;
import com.tencent.bk.job.common.paas.model.NotifyMessageDTO;
import com.tencent.bk.job.common.service.quota.ResourceQuotaCheckResultEnum;
import com.tencent.bk.job.common.tenant.TenantService;
import com.tencent.bk.job.manage.GlobalAppScopeMappingService;
import com.tencent.bk.job.manage.metrics.MetricsConstants;
import com.tencent.bk.job.manage.quota.limit.SendNotifyResourceQuotaManager;
import io.micrometer.core.instrument.MeterRegistry;
import io.micrometer.core.instrument.Tag;
import io.micrometer.core.instrument.Tags;
import io.micrometer.core.instrument.Timer;
import lombok.extern.slf4j.Slf4j;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.time.Duration;
import java.util.ArrayList;
import java.util.Set;
import java.util.concurrent.TimeUnit;

@Slf4j
@Service
public class WatchableSendMsgService {

    private final ICmsiClient cmsiApiClient;
    private final MeterRegistry meterRegistry;
    private final TenantService tenantService;
    private final SendNotifyResourceQuotaManager sendNotifyResourceQuotaManager;

    private int i = 0;

    @Autowired
    public WatchableSendMsgService(ICmsiClient cmsiApiClient,
                                   MeterRegistry meterRegistry,
                                   TenantService tenantService,
                                   SendNotifyResourceQuotaManager sendNotifyResourceQuotaManager) {
        this.cmsiApiClient = cmsiApiClient;
        this.meterRegistry = meterRegistry;
        this.tenantService = tenantService;
        this.sendNotifyResourceQuotaManager = sendNotifyResourceQuotaManager;
    }

    @EsbApiTimed
    public void sendMsgWithApp(
        Long appId,
        long createTimeMillis,
        String msgType,
        String sender,
        Set<String> receivers,
        String title,
        String content
    ) {
        String sendStatus = MetricsConstants.TAG_VALUE_SEND_STATUS_FAILED;
        try {
            log.debug("sendMsgWithApp begin, appId={}, msgType={}, sender={}, receivers={}, title={}, content={}",
                appId, msgType, sender, receivers, title, content);

            checkSendNotifyQuotaLimit(
                appId,
                sender
            );

            if (i % 2 == 0) {
                // 环境上的发送通知接口调不了，模拟成功
                log.debug("test send success, i={}", i);
                sendStatus = MetricsConstants.TAG_VALUE_SEND_STATUS_SUCCESS;
            } else {
                // 真正调用发送通知接口（会报错）
                log.debug("test send interface, i={}", i);

                NotifyMessageDTO notifyMessageDTO = buildNotifyMessage(sender, receivers, title, content);
                beginSendMessage(
                    msgType,
                    notifyMessageDTO,
                    tenantService.getTenantIdByAppId(appId)
                );
                sendStatus = MetricsConstants.TAG_VALUE_SEND_STATUS_SUCCESS;
            }
            i++;
        } catch (PaasException e) {
            sendNotifyLimitRollback(appId, sender);
            throw e;
        } catch (Exception e) {
            throw e;
        } finally {
            recordMetrics(createTimeMillis, msgType, sendStatus, appId);
        }
    }

    public void sendMsgWithCurrentTenant(
        String tenantId,
        long createTimeMillis,
        String msgType,
        String sender,
        Set<String> receivers,
        String title,
        String content
    ) {
        String sendStatus = MetricsConstants.TAG_VALUE_SEND_STATUS_FAILED;
        try {
            log.debug("sendMsgWithCurrentTenant begin, tenantId={}, msgType={}, sender={}, receivers={}, title={}, content={}",
                tenantId, msgType, sender, receivers, title, content);

            checkSendNotifyQuotaLimit(
                null,
                sender
            );

            if (i % 2 == 0) {
                // 环境上的发送通知接口调不了，模拟成功
                log.debug("sendMsgWithCurrentTenant. test send success, i={}", i);
                sendStatus = MetricsConstants.TAG_VALUE_SEND_STATUS_SUCCESS;
            } else {
                log.debug("sendMsgWithCurrentTenant. test send interface, i={}", i);

                NotifyMessageDTO notifyMessageDTO = buildNotifyMessage(sender, receivers, title, content);
                beginSendMessage(
                    msgType,
                    notifyMessageDTO,
                    tenantId
                );
                sendStatus = MetricsConstants.TAG_VALUE_SEND_STATUS_SUCCESS;
            }
        } catch (PaasException e) {
            sendNotifyLimitRollback(null, sender);
            throw e;
        } catch (Exception e) {
            throw e;
        } finally {
            recordMetrics(createTimeMillis, msgType, sendStatus, null);
        }

    }

    private void beginSendMessage(String msgType, NotifyMessageDTO notifyMessageDTO, String tenantId) {
        cmsiApiClient.sendMsg(
            msgType,
            notifyMessageDTO,
            tenantId
        );
    }

    private NotifyMessageDTO buildNotifyMessage(String sender,
                                    Set<String> receivers,
                                    String title,
                                    String content) {
        NotifyMessageDTO notifyMessageDTO = new NotifyMessageDTO();
        notifyMessageDTO.setTitle(title);
        notifyMessageDTO.setContent(content);
        notifyMessageDTO.setSender(sender);
        notifyMessageDTO.setReceiverUsername(new ArrayList<>(receivers));
        return notifyMessageDTO;
    }

    private String buildAppIdStr(Long appId) {
        return appId == null ? MetricsConstants.TAG_VALUE_APP_ID_NULL : appId.toString();
    }

    private void recordMetrics(Long createTimeMillis, String msgType, String sendStatus, Long appId) {
        long delayMillis = System.currentTimeMillis() - createTimeMillis;
        Tags tags = Tags.of(MetricsConstants.TAG_KEY_MSG_TYPE, msgType);
        tags = tags.and(MetricsConstants.TAG_KEY_SEND_STATUS, sendStatus);
        tags = tags.and(MetricsConstants.TAG_KEY_APP_ID, buildAppIdStr(appId));
        recordSendMsgDelay(delayMillis, tags);
    }

    private void recordSendMsgDelay(long delayMillis, Iterable<Tag> tags) {
        Timer.builder(MetricsConstants.NAME_NOTIFY_DELAY)
            .description("Notify Message Send Delay")
            .tags(tags)
            .publishPercentileHistogram(true)
            .minimumExpectedValue(Duration.ofMillis(100))
            .maximumExpectedValue(Duration.ofSeconds(60L))
            .register(meterRegistry)
            .record(delayMillis, TimeUnit.MILLISECONDS);
    }

    /*
     * 检查发送通知配额限制，超过限制抛出异常，不超过限制累加当天计数
     */
    private void checkSendNotifyQuotaLimit(Long appId, String triggerUser) {
        ResourceScope resourceScope = null;
        if (appId == null) {
            resourceScope = GlobalAppScopeMappingService.get().getScopeByAppId(appId);
        }
        ResourceQuotaCheckResultEnum checkResult = sendNotifyResourceQuotaManager.checkAndIncrement(
            resourceScope,
            triggerUser
        );
        switch (checkResult) {
            case NO_LIMIT:
                break;
            case RESOURCE_SCOPE_LIMIT:
                log.warn("Send notify exceed resource scope quota limit, resourceScope: {}",resourceScope);
                throw new ResourceExhaustedException(ErrorCode.SEND_NOTIFY_EXCEED_RESOURCE_SCOPE_QUOTA_LIMIT);
            case USER_LIMIT:
                log.warn("Send notify exceed user quota limit, triggerUser: {}", triggerUser);
                throw new ResourceExhaustedException(ErrorCode.SEND_NOTIFY_EXCEED_USER_QUOTA_LIMIT);
            case SYSTEM_LIMIT:
                log.warn("Send notify exceed system quota limit, resourceScope: {}, triggerUser: {}",
                    resourceScope, triggerUser);
                throw new ResourceExhaustedException(ErrorCode.SEND_NOTIFY_EXCEED_SYSTEM_QUOTA_LIMIT);
        }
    }

    /*
     * 因网络等原因发送通知出错，回滚配额限制
     */
    private void sendNotifyLimitRollback(Long appId, String triggerUser) {
        ResourceScope resourceScope = null;
        if (appId == null) {
            resourceScope = GlobalAppScopeMappingService.get().getScopeByAppId(appId);
        }
        sendNotifyResourceQuotaManager.rollback(
            resourceScope,
            triggerUser
        );
    }
}
