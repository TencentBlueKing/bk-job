package com.tencent.bk.job.file.worker.config;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.Profile;
import springfox.documentation.builders.PathSelectors;
import springfox.documentation.builders.RequestHandlerSelectors;
import springfox.documentation.spi.DocumentationType;
import springfox.documentation.spring.web.plugins.Docket;
import springfox.documentation.swagger2.annotations.EnableSwagger2;

import java.util.Arrays;
import java.util.HashSet;

@Configuration
@EnableSwagger2
@Profile({"dev", "test", "local"})
public class SwaggerConfig {

    private final WorkerConfig workerConfig;

    @Autowired
    public SwaggerConfig(WorkerConfig workerConfig) {
        this.workerConfig = workerConfig;
    }

    @Bean
    public Docket api() {
        return new Docket(DocumentationType.SWAGGER_2)
            .host(workerConfig.getSwaggerUrl() + "/job-file-worker")
            .protocols(new HashSet<>(Arrays.asList("http", "https")))
            .select()
            .apis(RequestHandlerSelectors.basePackage("com.tencent.bk.job.file.worker.api"))
            .paths(PathSelectors.any())
            .build();
    }
}
